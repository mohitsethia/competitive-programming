void solve(){
    int n, budget;
    cin >> n >> budget;
    vi a(n);
    for(int i = 0; i < n; i++) {
        cin >> a[i];
    }
    sort(a.begin(), a.end());
    int packets = 0;
    int sum = 0;
    for(int i = 0; i < n; i++) {
        if(sum + a[i] > budget) {
            n = i;
            break;
        }
        sum += a[i];
    }
    packets = n;
    int considered = 1;
    while(n > 0 && budget >= sum) {
        int canTake = (budget-sum)/n;
        packets += canTake*n;
        considered += canTake;
        sum += canTake*n;
        sum -= considered+a[n-1]-1;
        n--;
        // sum += n;
    }
    cout << packets << "\n";
}

